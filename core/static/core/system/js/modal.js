$('#FinalBudget').on('show.bs.modal', function (event) {
    var button = $(event.relatedTarget) 
    var budget_id = button.data('budget_id') 
    var patient_id = button.data('patient_id') 
    var modal = $(this)
    modal.find('.modal-title').text('Está seguro que desea aceptar el presupuesto número '+ budget_id)
    modal.find('#modal_budget').val(budget_id)
    modal.find('#modal_patient').val(patient_id)
    })
